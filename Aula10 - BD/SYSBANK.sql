CREATE TABLE CLIENTE(
	ID BIGINT PRIMARY KEY AUTO_INCREMENT,
	NOME VARCHAR(255),
	EMAIL VARCHAR(100) UNIQUE,
	SEXO ENUM("M","F")
);

CREATE TABLE PERFIL(
	ID BIGINT PRIMARY KEY,
	PROFISSAO VARCHAR(255),
	HOBBIES VARCHAR(255),
	GOSTO_MUSICAL VARCHAR(255),
	FOREIGN KEY(ID) REFERENCES CLIENTE(ID)
);