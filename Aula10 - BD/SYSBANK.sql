CREATE TABLE AGENCIA(
	ID BIGINT PRIMARY KEY AUTO_INCREMENT,
	NOME VARCHAR(50),
	CODIGO VARCHAR(4),
	BANCO_ID BIGINT,
	FOREIGN KEY(BANCO_ID) REFERENCES BANCO(ID),
	UNIQUE INDEX(CODIGO,BANCO_ID)
);

